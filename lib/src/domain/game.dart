class Game {
  final int id;
  final String name;
  final String price;
  final String players;
  final String type;
  final String image;
  final String backgroundImage;

  Game({
    required this.id,
    required this.name,
    required this.price,
    required this.players,
    required this.type,
    required this.image,
    required this.backgroundImage,
  });
}
